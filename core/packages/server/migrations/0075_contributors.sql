-- 0075_contributors.sql — /support 贡献者名单(issue #28)。admin 手录姓名/WCA ID,
-- score = 贡献次数(每次提交反馈/bug/建议 +1,admin 点数字自增)。公开 GET 按 score 降序。
CREATE TABLE contributors (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name        VARCHAR(200) NOT NULL,
  wca_id      VARCHAR(20),
  avatar_url  TEXT,
  score       INTEGER      NOT NULL DEFAULT 1,
  created_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW(),
  updated_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);
CREATE INDEX idx_contributors_score ON contributors(score DESC, created_at);
CREATE TRIGGER contributors_updated_at BEFORE UPDATE ON contributors
  FOR EACH ROW EXECUTE FUNCTION trg_set_updated_at();
