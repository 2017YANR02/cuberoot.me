-- 0001_nav_sites.sql — /site 网址导航,从 client/src/pages/sites/data/sites.ts 迁来
-- 旧字符串 id (r002 / r003) 不带过去,自增 BIGINT;group 列叫 group_id 避开 SQL 关键字。
CREATE TABLE nav_sites (
  id          BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  group_id    VARCHAR(20)  NOT NULL,
  position    INTEGER      NOT NULL,
  name        VARCHAR(200) NOT NULL,
  name_en     VARCHAR(200),
  name_zh     VARCHAR(200),
  url         TEXT         NOT NULL,
  alt_urls    JSONB,
  author      VARCHAR(200),
  desc_en     TEXT,
  desc_zh     TEXT,
  youtube     TEXT,
  tags        JSONB,
  status      VARCHAR(20),
  updated_at  TIMESTAMPTZ  NOT NULL DEFAULT NOW()
);
CREATE INDEX idx_nav_sites_group_pos ON nav_sites(group_id, position);
CREATE TRIGGER nav_sites_updated_at BEFORE UPDATE ON nav_sites
  FOR EACH ROW EXECUTE FUNCTION trg_set_updated_at();
