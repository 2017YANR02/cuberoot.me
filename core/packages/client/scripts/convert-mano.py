"""
MANO pkl -> web hand asset (JSON + base64 buffers) for /sim hands.

Usage (uv, per global convention):
  uv run core/packages/client/scripts/convert-mano.py [--src D:/cube/mano] [--no-subdiv]

Input:  MANO_RIGHT.pkl (searched under --src, its models/ subdir, or inside
        mano_v1_2.zip found there). This is the licensed MPI file the user
        downloads from https://mano.is.tue.mpg.de after registration — NEVER
        commit it or the converted output (repo is public; MANO license
        forbids redistribution). Output dir is gitignored.
        The LEFT asset is generated by mirroring RIGHT across x=0. The shipped
        MANO_LEFT template is itself a bit-exact mirror of RIGHT (verified
        2026-07-11: max vert/joint/weight diff 0.0) — mirroring in-converter
        just drops the second pkl and keeps L/R symmetry true by construction.
        (An earlier "left template is asymmetric" diagnosis was wrong: the
        measured fGap difference came from a wrist-anchor offset bug in
        homeLeft, since fixed — see handPoses.ts homeLeft mano branch.)
Output: core/packages/client/public/sim/hands/mano/{right,left}.mano.json

Since @4 SMPLX_NEUTRAL.npz is REQUIRED: the real forearm (cut from the SMPL-X
        body, scale-matched by each template's own middle-finger length) is
        FUSED into the hand mesh at convert time (wrist rings zip-bridged into
        one closed mesh, forearm verts skinned to a new 26th "forearm" bone
        pivoting at the wrist). One mesh = one skinning = one texture bake =
        seamless wrist, matching the upstream single-body look (两件套时代的
        腕缝台阶 / 贴图断层 / stumpFit 单位混用 bug 一并根除,2026-07-11)。
        Also emits smplx/fullbody.smplx.json (debug viewer, raw T-pose body).
        Same license rule as MANO: never commit the npz or converted output.

What it does (bind pose = MANO zero pose, betas = 0):
  1. Unpickle with a chumpy stub (original pkls reference chumpy classes;
     we only need the wrapped ndarrays — no chumpy install, no numpy pin).
  2. joints = J_regressor @ v_template  (16: wrist + 5 fingers x 3).
  3. Identify finger chains geometrically (thumb = chain base closest to
     wrist; index..pinky ordered by lateral position, index on thumb side)
     — robust to MANO's odd index/middle/pinky/ring/thumb joint order.
  4. Synthesize the WebXR joints MANO lacks: four-finger metacarpals
     (wrist + 22% toward MCP, zero skin weight — FK anchors for the rig's
     palm-arch meta channel) and five tips (extremal skin vertex along the
     distal bone axis). Result: the exact 25-name WebXR set, so the
     existing adaptGltfHand() consumes this asset unchanged.
  5. Cap the open wrist ring (fan to centroid, skinned to wrist) so the
     mesh is closed; then one Loop subdivision step (778 -> ~3.1k verts,
     silhouette-smooth; skin weights averaged + renormalized to top-4).
  6. Injective UV atlas via box projection (MANO has no authored UV layout):
     faces bucketed by dominant normal axis (6 directions), split into
     edge-connected components, each component projected onto its axis plane
     and shelf-packed with gutters; seam vertices are duplicated per
     component. This is what lets bakeHandTextures (UV-space rasterization,
     3D-domain feature evaluation) run on MANO — an overlapping projection
     would blend palm/dorsal features into the same texels.
  7. Smooth area-weighted vertex normals exported explicitly (computed
     BEFORE seam duplication — computeVertexNormals on the duplicated mesh
     would produce hard shading seams along every UV island border).
"""
