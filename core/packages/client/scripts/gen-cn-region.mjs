// 从 cubingchina 的 data.sql(repo 内的 region 种子表)生成「中国地名 → 省份」静态映射,
// 供选手页「去过的省份」客户端聚合用。region 表是树:省份 pid=1,城市 pid=省份id,HK/MO/TW pid=0。
// 用法: node packages/client/scripts/gen-cn-region.mjs [path/to/cubingchina/data.sql]
// 默认读 D:/cube/cubingchina/data.sql,输出覆盖 packages/client/lib/data/cn-region.ts。
// 新比赛在新城市时(WCA city 段匹配不到)会回退到国家「中国」;补城市就重跑本脚本。

import { readFile, writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import * as OpenCC from 'opencc-js';

// cubingchina data.sql 的 name_zh 对港澳台是繁体(澳門 / 中華台北),全站简体专用 → 一律 t2s 转简(对简体幂等)。
const t2s = OpenCC.Converter({ from: 't', to: 'cn' });

const __dirname = dirname(fileURLToPath(import.meta.url));
const dataSqlPath = process.argv[2] || 'D:/cube/cubingchina/data.sql';
const outPath = join(__dirname, '../lib/data/cn-region.ts');

// 与客户端 person-misc-region.ts 必须用同一套规范化(去所有非字母数字 + 转小写)
const norm = (s) => s.toLowerCase().replace(/[^a-z0-9]+/g, '');

const sql = await readFile(dataSqlPath, 'utf8');

// 取 region 的 INSERT ... VALUES (...),(...),...; 块
const m = sql.match(/INSERT INTO `region`[^;]*?VALUES\s*([\s\S]*?);/i);
if (!m) { console.error('region INSERT not found in', dataSqlPath); process.exit(1); }

// 解析每行 (id, 'name', 'name_zh', pid, lon, lat, offset)
const rowRe = /\(\s*(\d+)\s*,\s*'((?:[^'\\]|\\.)*)'\s*,\s*'((?:[^'\\]|\\.)*)'\s*,\s*(\d+)/g;
const rows = [];
let r;
while ((r = rowRe.exec(m[1])) !== null) {
  rows.push({ id: Number(r[1]), en: r[2].replace(/\\'/g, "'"), zh: t2s(r[3].replace(/\\'/g, "'")), pid: Number(r[4]) });
}

// 省份 = pid===1
const provinceById = new Map();
for (const row of rows) if (row.pid === 1) provinceById.set(row.id, { zh: row.zh, en: row.en });

// map1: 规范化地名 → 所属省份 {zh,en}(省份本身 + 各城市指向其省份 + 省份带 " Province" 变体)
const out = {};
const put = (key, prov) => { const k = norm(key); if (k && !(k in out)) out[k] = prov; };
for (const [, prov] of provinceById) { put(prov.en, prov); put(prov.en + ' Province', prov); put(prov.zh, prov); }
for (const row of rows) {
  const prov = provinceById.get(row.pid); // 城市:pid 指向省份
  if (prov) { put(row.en, prov); put(row.zh, prov); }
}

// map2: 规范化英文/拼音地名 → 该地自身简体名(城市 / 省份 / 港澳台都收)。/zh 下本地化地名用。
const zhOut = {};
const putZh = (key, zh) => { const k = norm(key); if (k && !(k in zhOut)) zhOut[k] = zh; };
for (const row of rows) putZh(row.en, row.zh);
for (const [, prov] of provinceById) putZh(prov.en + ' Province', prov.zh);

// map3: 同拼音异地城市消歧(如 taizhou = 台州/浙江 vs 泰州/江苏,suzhou = 苏州/江苏 vs 宿州/安徽)。
//   扁平 zhOut 逐拼音只能存一个,碰撞时另一个被静默丢弃。此表按「城市拼音|省份拼音」收录所有碰撞城市
//   的两侧,消费端(city-localize)拿到城市串里的省份段即可选对。仅收真正碰撞的城市,不膨胀。
const cityByPinyin = new Map(); // norm(cityEn) → [{ zh, provNorm }]
for (const row of rows) {
  const prov = provinceById.get(row.pid); // 只看城市(pid 指向省份)
  if (!prov) continue;
  const k = norm(row.en);
  if (!cityByPinyin.has(k)) cityByPinyin.set(k, []);
  cityByPinyin.get(k).push({ zh: row.zh, provNorm: norm(prov.en) });
}
const disambig = {};
for (const [k, arr] of cityByPinyin) {
  if (new Set(arr.map((a) => a.zh)).size < 2) continue; // 无碰撞跳过
  for (const a of arr) disambig[`${k}|${a.provNorm}`] = a.zh; // 同 (城,省) 幂等
}

const entries = Object.entries(out).sort(([a], [b]) => a.localeCompare(b));
const body = entries.map(([k, v]) => `  ${JSON.stringify(k)}: { zh: ${JSON.stringify(v.zh)}, en: ${JSON.stringify(v.en)} },`).join('\n');
const zhEntries = Object.entries(zhOut).sort(([a], [b]) => a.localeCompare(b));
const zhBody = zhEntries.map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`).join('\n');
const disEntries = Object.entries(disambig).sort(([a], [b]) => a.localeCompare(b));
const disBody = disEntries.map(([k, v]) => `  ${JSON.stringify(k)}: ${JSON.stringify(v)},`).join('\n');

const file = `// AUTO-GENERATED by scripts/gen-cn-region.mjs from cubingchina data.sql — 勿手改。
// CN_PLACE_PROVINCE: 规范化中国地名(省份 / 城市,去非字母数字 + 小写)→ 所属省份(简体 + 英文)。
//   选手页「去过的省份」用:WCA comp.city 各段规范化后查此表;查不到回退国家「中国」。
// CN_PLACE_ZH: 规范化英文/拼音地名 → 该地自身简体名(城市 / 省份 / 港澳台);/zh 下本地化城市名用。
// CN_PLACE_DISAMBIG: 同拼音异地城市消歧,key = \`城市拼音|省份拼音\` → 该城简体名。扁平 CN_PLACE_ZH
//   每个拼音只存一个,碰撞时另一城被丢弃;消费端有省份段时先查此表选对(台州/泰州、苏州/宿州 等)。
// 重新生成:node packages/client/scripts/gen-cn-region.mjs [data.sql]

export const CN_PLACE_PROVINCE: Record<string, { zh: string; en: string }> = {
${body}
};

export const CN_PLACE_ZH: Record<string, string> = {
${zhBody}
};

export const CN_PLACE_DISAMBIG: Record<string, string> = {
${disBody}
};
`;

await writeFile(outPath, file, 'utf8');
console.log(`wrote ${entries.length} province keys + ${zhEntries.length} zh keys + ${disEntries.length} disambig keys (${provinceById.size} provinces) → ${outPath}`);
