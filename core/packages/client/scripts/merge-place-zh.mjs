// Merge GeoNames-authoritative cities (place-geo.json) + LLM batch outputs (out/batch-*.json)
// → committed LLM layer scripts/place-tail-zh.json + generated module lib/data/place-zh.ts.
// Run from core/packages/client (needs opencc-js). t2s every value (no-Traditional safety net).
import fs from 'node:fs';
import * as OpenCC from 'opencc-js';
const t2s = OpenCC.Converter({ from: 't', to: 'cn' });
const GN = 'D:/cube/cuberoot.me/.tmp/geonames';
const HERE = 'D:/cube/cuberoot.me/core/packages/client';

const geo = JSON.parse(fs.readFileSync(`${GN}/place-geo.json`, 'utf8'));
const cityKeys = new Set(geo.llmCity.map((c) => c.key));
const adminKeys = new Set(geo.llmAdmin.map((c) => c.key));

// committed LLM layer (key -> zh). Bootstrapped once from .tmp/geonames/out/*; maintained here.
const llm = JSON.parse(fs.readFileSync(`${HERE}/scripts/place-tail-zh.json`, 'utf8'));

// sanitize: bare 台湾 → 中国台湾 (politically consistent with 中华台北/中国香港 used site-wide)
const sani = (s) => t2s(s).replace(/(?<!中国)台湾/g, '中国台湾');

// build final maps
const cityMap = {}, adminMap = {};
for (const [k, v] of Object.entries(geo.cityZh)) cityMap[k] = sani(v);     // GeoNames authoritative
for (const k of cityKeys) if (llm[k]) cityMap[k] = sani(llm[k]);            // LLM city (overrides/fills)
for (const k of adminKeys) if (llm[k]) adminMap[k] = sani(llm[k]);

// ── validation ─────────────────────────────────────────────────────────
const trad = /[後濾華薩徹溫內裡龍實畫鄉裏寧靜麗淨齊敗醜團]/; // quick traditional spot-set
const hasCjk = (s) => /[一-鿿]/.test(s);
const missing = [], stillTrad = [], noCjk = [], tw = [];
for (const k of cityKeys) if (!cityMap[k]) missing.push(`city ${k}`);
for (const k of adminKeys) if (!adminMap[k]) missing.push(`admin ${k}`);
for (const [map, name] of [[cityMap, 'city'], [adminMap, 'admin']]) {
  for (const [k, v] of Object.entries(map)) {
    if (trad.test(v)) stillTrad.push(`${name} ${k}=${v}`);
    if (!hasCjk(v)) noCjk.push(`${name} ${k}=${v}`);
    if (/台湾/.test(v)) tw.push(`${name} ${k}=${v}`);
  }
}

// ── write generated module ──────────────────────────────────────────────
const emit = (o) => Object.keys(o).sort().map((k) => `  ${JSON.stringify(k)}: ${JSON.stringify(o[k])},`).join('\n');
const ts = `// AUTO-GENERATED by scripts/gen-place-zh.mjs + merge-place-zh.mjs — 勿手改。
// 全球比赛城市 / 行政区英文段 → 简体中文。key = \`\${ISO2}:\${normSeg}\`
//   normSeg = s.normalize('NFD').replace(/[̀-ͯ]/g,'').toLowerCase().replace(/[^a-z0-9]+/g,'')
// 来源:GeoNames alternateNames(zh,OpenCC t2s 转简)按坐标反查 + LLM 音译/通名兜底
//   (committed: scripts/place-tail-zh.json)。大中华区(CN/HK/MO/TW)不在此,走 cn-region。
// 重新生成:见 scripts/gen-place-zh.mjs 顶部。
export const PLACE_CITY_ZH: Record<string, string> = {
${emit(cityMap)}
};
export const PLACE_ADMIN_ZH: Record<string, string> = {
${emit(adminMap)}
};
`;
fs.mkdirSync(`${HERE}/lib/data`, { recursive: true });
fs.writeFileSync(`${HERE}/lib/data/place-zh.ts`, ts);

console.log(`PLACE_CITY_ZH: ${Object.keys(cityMap).length}  (GeoNames ${Object.keys(geo.cityZh).length} + LLM ${cityKeys.size})`);
console.log(`PLACE_ADMIN_ZH: ${Object.keys(adminMap).length}`);
console.log(`place-tail-zh.json (LLM layer): ${Object.keys(llm).length}`);
console.log(`\nVALIDATION  missing=${missing.length} traditional=${stillTrad.length} noCjk=${noCjk.length} taiwan=${tw.length}`);
if (missing.length) console.log('MISSING:', missing.slice(0, 20));
if (stillTrad.length) console.log('TRADITIONAL:', stillTrad.slice(0, 20));
if (noCjk.length) console.log('NO-CJK:', noCjk.slice(0, 30));
if (tw.length) console.log('TAIWAN(now 中国台湾):', tw.slice(0, 10));
