// Panel 4: Cycle index inspector — symbolic display with animated substitution
// ══════════════════════════════════════════════════════════════════════════════

function CycleIndexPanel({ lang }: { lang: Lang }) {
  const [k, setK] = useState(2);
  const [highlighted, setHighlighted] = useState<number | null>(null);

  // The 5 terms of the face cycle index (coefficient, monoial display, c value)
  const TERMS: { coeff: number; mono: string; c: number; classIdx: number }[] = [
    { coeff: 1,  mono: 'x₁⁶',        c: 6, classIdx: 0 },
    { coeff: 6,  mono: 'x₁² x₄',     c: 3, classIdx: 1 },
    { coeff: 3,  mono: 'x₁² x₂²',    c: 4, classIdx: 2 },
    { coeff: 8,  mono: 'x₃²',         c: 2, classIdx: 3 },
    { coeff: 6,  mono: 'x₂³',         c: 3, classIdx: 4 },
  ];

  const termValues = TERMS.map(t => t.coeff * Math.pow(k, t.c));
  const total = termValues.reduce((a, b) => a + b, 0);
  const answer = total / 24;

  return (
    <div className="gt-panel" style={{ marginTop: 36 }}>
      <div className="gt-panel-title">
        <L zh="圈指标检视器" en="Cycle index inspector" />
      </div>
      <div className="gt-panel-sub">
        <L
          zh="悬停或点击每项，高亮其对应的共轭类；拖动 k 观察代入 x_i → k 后的数值变化。"
          en="Hover or click each term to highlight its conjugacy class; drag k to watch the substitution x_i → k animate."
        />
      </div>

      <div className="gt-panel-input-row" style={{ gap: 10, alignItems: 'center', flexWrap: 'wrap' }}>
        <label style={{ fontFamily: 'var(--mono)', fontSize: 13 }}>k =</label>
        <input
          type="range" min={1} max={8} value={k}
          onChange={e => setK(+e.target.value)}
          style={{ flex: '1 1 100px' }}
          className="gt-input"
        />
        <span style={{ fontFamily: 'var(--mono)', fontWeight: 700, color: 'var(--accent)', minWidth: 20 }}>{k}</span>
      </div>

      {/* Polynomial display */}
      <div style={{ marginTop: 20, fontFamily: 'var(--mono)', fontSize: 14, lineHeight: 2.2, overflowX: 'auto' }}>
        <div style={{ display: 'flex', flexWrap: 'wrap', alignItems: 'baseline', gap: '0 4px' }}>
          <span style={{ color: 'var(--ink-dim)' }}>Z = (1/24)(</span>
          {TERMS.map((t, i) => {
            const isHL = highlighted === i;
            const col = PALETTE[t.classIdx % PALETTE.length];
            return (
              <span key={i}
                style={{
                  cursor: 'pointer',
                  color: isHL ? col : 'var(--ink)',
                  background: isHL ? `color-mix(in srgb, ${col} 12%, transparent)` : 'transparent',
                  borderRadius: 4,
                  padding: '0 4px',
                  fontWeight: isHL ? 700 : 400,
                  transition: 'all .15s',
                  display: 'inline-flex',
                  flexDirection: 'column',
                  alignItems: 'center',
                }}
                onMouseEnter={() => setHighlighted(i)}
                onMouseLeave={() => setHighlighted(null)}
                onClick={() => setHighlighted(prev => prev === i ? null : i)}
              >
                <span>{t.coeff > 1 ? `${t.coeff} ` : ''}{t.mono}</span>
                {/* Substituted value fades in */}
                <span style={{
                  fontSize: 10,
                  color: isHL ? col : 'var(--ink-faint)',
                  minHeight: '1em',
                  transition: 'opacity .2s',
                  opacity: 1,
                }}>
                  = {t.coeff}·{k}^{t.c} = {termValues[i]}
                </span>
              </span>
            );
          }).reduce<React.ReactNode[]>((acc, el, i) => {
            if (i > 0) acc.push(<span key={`+${i}`} style={{ color: 'var(--ink-dim)' }}> + </span>);
            acc.push(el);
            return acc;
          }, [])}
          <span style={{ color: 'var(--ink-dim)' }}>)</span>
        </div>

        {/* Substituted line */}
        <div style={{ marginTop: 8, color: 'var(--ink-dim)', fontSize: 13 }}>
          <L zh="代入 x_i = k：" en="Substituting x_i = k:" />
          <span style={{ color: 'var(--ink)', fontWeight: 600, marginLeft: 6 }}>
            ({termValues.join(' + ')}) / 24 = {total} / 24 ={' '}
            <span style={{ color: 'var(--green)', fontWeight: 700, fontSize: 16 }}>{answer}</span>
          </span>
        </div>
      </div>

      {highlighted !== null && (
        <div className="gt-aside" style={{ marginTop: 12, fontSize: 13 }}>
          <L
            zh={<>
              <strong style={{ color: PALETTE[TERMS[highlighted].classIdx % PALETTE.length] }}>
                {FACE_CLASSES[TERMS[highlighted].classIdx].nameZh}
              </strong>（{FACE_CLASSES[TERMS[highlighted].classIdx].size} 个旋转，圈型 {FACE_CLASSES[TERMS[highlighted].classIdx].cycleType}）：
              圈指标中的单项式 <strong>{TERMS[highlighted].mono}</strong> 记录了这类旋转的圈结构；
              代入 x_i = k 后，该项变为 {TERMS[highlighted].coeff}·k^{TERMS[highlighted].c} = {termValues[highlighted]}。
            </>}
            en={<>
              <strong style={{ color: PALETTE[TERMS[highlighted].classIdx % PALETTE.length] }}>
                {FACE_CLASSES[TERMS[highlighted].classIdx].nameEn}
              </strong> ({FACE_CLASSES[TERMS[highlighted].classIdx].size} rotations, cycle type {FACE_CLASSES[TERMS[highlighted].classIdx].cycleType}):
              the monomial <strong>{TERMS[highlighted].mono}</strong> in the cycle index records this class's cycle structure;
              substituting x_i = k gives {TERMS[highlighted].coeff}·k^{TERMS[highlighted].c} = {termValues[highlighted]}.
            </>}
          />
        </div>
      )}

      {/* Class-to-term cross-reference table */}
      <div style={{ overflowX: 'auto', marginTop: 20 }}>
        <table className="gt-compare">
          <thead>
            <tr>
              <th><L zh="共轭类" en="Conjugacy class" /></th>
              <th><L zh="大小" en="Size" /></th>
              <th><L zh="圈型 (面)" en="Cycle type (faces)" /></th>
              <th><L zh="圈数 c" en="Cycles c" /></th>
              <th><L zh={`k^c (k=${k})`} en={`k^c (k=${k})`} /></th>
              <th><L zh="贡献" en="Contribution" /></th>
            </tr>
          </thead>
          <tbody>
            {FACE_CLASSES.map((cls, i) => (
              <tr
                key={i}
                style={{
                  cursor: 'pointer',
                  background: highlighted === i
                    ? `color-mix(in srgb, ${PALETTE[i % PALETTE.length]} 10%, transparent)`
                    : undefined,
                }}
                onMouseEnter={() => setHighlighted(i)}
                onMouseLeave={() => setHighlighted(null)}
              >
                <td style={{ color: PALETTE[i % PALETTE.length], fontWeight: 600 }}>
                  {lang === 'zh' ? cls.nameZh : cls.nameEn}
                </td>
                <td style={{ textAlign: 'center', fontFamily: 'var(--mono)' }}>{cls.size}</td>
                <td style={{ textAlign: 'center', fontFamily: 'var(--mono)' }}>{cls.cycleType}</td>
                <td style={{ textAlign: 'center', fontFamily: 'var(--mono)' }}>{cls.cycles}</td>
                <td style={{ textAlign: 'center', fontFamily: 'var(--mono)' }}>
                  {Math.pow(k, cls.cycles)}
                </td>
                <td style={{ textAlign: 'center', fontFamily: 'var(--mono)', fontWeight: 700 }}>
                  {termValues[i]}
                </td>
              </tr>
            ))}
            <tr style={{ borderTop: '2px solid var(--rule)', fontWeight: 700 }}>
              <td colSpan={5} style={{ textAlign: 'right', paddingRight: 12, fontFamily: 'var(--mono)' }}>
                <L zh="总和 / 24 =" en="Sum / 24 =" />
              </td>
              <td style={{ textAlign: 'center', fontFamily: 'var(--mono)', color: 'var(--green)', fontSize: 15 }}>
                {total} / 24 = {answer}
              </td>
            </tr>
          </tbody>
        </table>
      </div>
    </div>
  );
}
