// Closed-form formulas (verified against the per-class Burnside sums)
// N(k) = (k^6 + 3k^4 + 12k^3 + 8k^2)/24
function faceFormula(k: number): number {
  return (Math.pow(k, 6) + 3 * Math.pow(k, 4) + 12 * Math.pow(k, 3) + 8 * Math.pow(k, 2)) / 24;
}
// E(k) = (k^12 + 6k^7 + 3k^6 + 8k^4 + 6k^3)/24
function edgeFormula(k: number): number {
  return (Math.pow(k, 12) + 6 * Math.pow(k, 7) + 3 * Math.pow(k, 6) + 8 * Math.pow(k, 4) + 6 * Math.pow(k, 3)) / 24;
}
// V(k) = (k^8 + 17k^4 + 6k^2)/24
function vertexFormula(k: number): number {
  return (Math.pow(k, 8) + 17 * Math.pow(k, 4) + 6 * Math.pow(k, 2)) / 24;
}

// The 24 face permutations (indices 0..5 = +x,-x,+y,-y,+z,-z = R,L,U,D,F,B)
// Generated by standard 90° face turns: Rx, Ry, Rz and their inverses
// Encoded as length-6 arrays: perm[i] = which old face goes to position i
function buildFacePerms(): number[][] {
  // Face index: 0=R(+x), 1=L(-x), 2=U(+y), 3=D(-y), 4=F(+z), 5=B(-z)
  // Rx (90° CW around +x axis): y->z, z->-y  =>  U->F, F->D, D->B, B->U; R,L fixed
  const rx = [0, 1, 5, 4, 2, 3];
  // Ry (90° CW around +y axis): z->x, x->-z  =>  F->R, R->B, B->L, L->F; U,D fixed
  const ry = [4, 5, 2, 3, 1, 0];
  // Rz (90° CW around +z axis): x->y, y->-x  =>  R->U, U->L, L->D, D->R; F,B fixed
  const rz = [3, 2, 0, 1, 4, 5];

  function compose(a: number[], b: number[]): number[] {
    return a.map((_, i) => b[a[i]]);
  }
  function pow3(m: number[]): number[] { return compose(compose(m, m), m); }

  const id = [0, 1, 2, 3, 4, 5];
  const gens = [rx, ry, rz, pow3(rx), pow3(ry), pow3(rz)];

  const perms: number[][] = [id];
  const seen = new Set<string>();
  seen.add(id.join(','));

  let frontier = [id];
  while (frontier.length > 0) {
    const next: number[][] = [];
    for (const p of frontier) {
      for (const g of gens) {
        const np = compose(p, g);
        const key = np.join(',');
        if (!seen.has(key)) {
          seen.add(key);
          perms.push(np);
          next.push(np);
        }
      }
    }
    frontier = next;
  }
  return perms.slice(0, 24);
}

const FACE_PERMS: number[][] = buildFacePerms();

function applyPerm(perm: number[], colors: number[]): number[] {
  return perm.map(i => colors[i]);
}

function orbitOf(colors: number[]): string[] {
  const seen = new Set<string>();
  for (const p of FACE_PERMS) {
    seen.add(applyPerm(p, colors).join(','));
  }
  return Array.from(seen);
}

